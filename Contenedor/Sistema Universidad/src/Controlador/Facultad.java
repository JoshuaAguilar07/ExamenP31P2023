package Controlador;


public class Facultad {

    private String codigo_facultad = "";
    private String nombre_facultad = "";
    private String estado_facultad = "";

    /**Cog
     * @return the codigo_facultad
     */
    public String getCodigo_facultad() {
        return codigo_facultad;
    }

    /**
     * @param codigo_facultad the codigo_facultad to set
     */
    public void setCodigo_facultad(String codigo_facultad) {
        this.codigo_facultad = codigo_facultad;
    }

    /**
     * @return the nombre_facultad
     */
    public String getNombre_facultad() {
        return nombre_facultad;
    }

    /**
     * @param nombre_facultad the nombre_facultad to set
     */
    public void setNombre_facultad(String nombre_facultad) {
        this.nombre_facultad = nombre_facultad;
    }

    /**
     * @return the estado_facultad
     */
    public String getEstado_facultad() {
        return estado_facultad;
    }

    /**
     * @param estado_facultad the estado_facultad to set
     */
    public void setEstado_facultad(String estado_facultad) {
        this.estado_facultad = estado_facultad;
    }

}
